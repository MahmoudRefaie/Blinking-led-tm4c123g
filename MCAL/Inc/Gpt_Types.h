// Numeric ID of a GPT channel
typedef enum{
    GPT_TIMER_0 = 0UL,
    GPT_TIMER_1 = 1UL,
    GPT_TIMER_2 = 2UL,
    GPT_TIMER_3 = 3UL,
    GPT_TIMER_4 = 4UL,
    GPT_TIMER_5 = 5UL
} Gpt_ChannelType;

// Type for reading and setting the timer values (in number of ticks)
typedef uint32 Gpt_ValueType;

typedef enum{
    GPT_MODE_PERIODIC = 100,
    GPT_MODE_ONE_SHOT
} Gpt_ModeType;

typedef enum{
    GPT_PREDEF_TIMER_1US_16BIT,
    GPT_PREDEF_TIMER_1US_24BIT,
    GPT_PREDEF_TIMER_1US_32BIT,
    GPT_PREDEF_TIMER_100US_32BIT,
}Gpt_PredefTimerType;

typedef struct{
    Gpt_ChannelType channelId;
    uint32 tickFrequency;
    uint32 tickValueMax;
    Gpt_ModeType mode;
}Gpt_ConfigType;







